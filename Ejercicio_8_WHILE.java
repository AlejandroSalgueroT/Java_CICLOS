public class Ejercicio_8_WHILE {
    public static void main(String[] args) {
         //8.	Mostrar en pantalla los primeros 20 múltiplos de 3.
         int contador=0;
         System.out.println("Los primeros 20 múltiplos de 3 son: ");
         while (contador<20) {
            contador++;
            int mult=contador*3;
            System.out.println(mult);
         
 
     }
    }
}
